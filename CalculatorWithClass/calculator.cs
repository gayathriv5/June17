namespace CalculatorFunction
{
    internal class Calc
    {
        public int Add(int i, int j)
        {
            return i + j;
        }

        public int Subtract(int i, int j)
        {
            return i - j;
        }

        public int Multiply(int i, int j)
        {
            return i * j;
        }

        public int Divide(int i, int j)
        {
            return i / j;
        }
    }
}
